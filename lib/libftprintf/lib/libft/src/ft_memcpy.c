#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t		i;
	char		*d;
	const char	*s;

	d = dst;
	s = src;
	i = -1;
	while (++i < n)
		d[i] = s[i];
	return (d);
}
