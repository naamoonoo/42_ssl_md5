#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int	i;

	i = -1;
	while (s1[++i])
		if (s1[i] == s2[0])
			if (ft_strncmp((char *)&s1[i], s2, ft_strlen(s2)) == 0)
				return ((char *)&s1[i]);
	return (s2[0] ? NULL : (char *)s1);
}
