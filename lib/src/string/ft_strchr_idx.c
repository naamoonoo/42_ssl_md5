#include "libft.h"

int	ft_strchr_idx(const char *s, int c)
{
	int i;
	int len;

	i = -1;
	len = ft_strlen(s);
	if ((char)c == '\0')
		return (0);
	while (s[++i])
		if (s[i] == (char)c)
			return (i);
	return (-1);
}
